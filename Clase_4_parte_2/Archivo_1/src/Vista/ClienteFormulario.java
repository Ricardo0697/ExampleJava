    private void jMenu2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu2MouseClicked
        // TODO add your handling code here:
        int selectRow = jTable1.getSelectedRow();
        if (selectRow >= 0) {
            String id = jTable1.getModel().getValueAt(selectRow, 0).toString();
            String nombre = jTable1.getModel().getValueAt(selectRow, 1).toString();
            String apellido = jTable1.getModel().getValueAt(selectRow, 2).toString();
            Cliente cliente = new Cliente();
            cliente.setId(Integer.parseInt(id));
            cliente.setNombre(nombre);
            cliente.setApellido(apellido);
            ClienteEditarFomulario clienteEditarFomulario = new ClienteEditarFomulario();
            clienteEditarFomulario.setCliente(cliente);
            clienteEditarFomulario.setVisible(true);
            clienteEditarFomulario.setAlwaysOnTop(true);
            clienteEditarFomulario.SetData();
            clienteEditarFomulario.addWindowListener(new WindowAdapter() {
                @Override
                public void windowClosing(WindowEvent ev) {
                    clienteEditarFomulario.setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
                    clienteEditarFomulario.setVisible(false);
                    if (clienteEditarFomulario.isAceptar()) {
                        CargarDatos();
                    }
                }
            });
        }
    }//GEN-LAST:event_jMenu2MouseClicked

    private void jMenu3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu3MouseClicked
        // TODO add your handling code here:
        int selectRow = jTable1.getSelectedRow();
        if (selectRow >= 0) {
            int confirm = JOptionPane.showOptionDialog(this,
                    "Seguro de eliminar el registro",
                    "Confirmaci√≥n", JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE, null, null, null);
            if (confirm == JOptionPane.YES_OPTION) {
                ClienteLogica ClienteLogica = new ClienteLogica();
                Cliente cliente = new Cliente();
                String id = jTable1.getModel().getValueAt(selectRow, 0).toString();
                cliente.setId(Integer.parseInt(id));
                ClienteLogica.Eliminar(cliente);
                CargarDatos();
            }
        }
    }//GEN-LAST:event_jMenu3MouseClicked

