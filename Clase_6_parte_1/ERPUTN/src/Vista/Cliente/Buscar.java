    public void CargarDatos(Cliente cliente) {

        jTable1 = new javax.swing.JTable() {
            public boolean editCellAt(int row, int column, java.util.EventObject e) {
                return false;
            }
        };;
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Id", "Cedula", "Nombre", "DirecciÃ³n"
                }
        ));

        jTable1.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);

        jTable1.setUpdateSelectionOnSort(false);

        jTable1.setVerifyInputWhenFocusTarget(false);

        this.jScrollPane2.setViewportView(jTable1);

        if (jTable1.getColumnModel().getColumnCount() > 0) {
            jTable1.getColumnModel().getColumn(2).setResizable(false);
        }

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        int rowCount = modelo.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }

        ClienteLogica clienteLogica = new ClienteLogica();

        modelo = (DefaultTableModel) jTable1.getModel();
        List<Cliente> clientes = clienteLogica.Buscar(cliente);
        for (int i = 0; i < clientes.size(); i++) {
            Object[] fila = new Object[4];
            fila[0] = ((Cliente) clientes.get(i)).getId();
            fila[1] = ((Cliente) clientes.get(i)).getCedula();
            fila[2] = ((Cliente) clientes.get(i)).getNombre();
            fila[3] = ((Cliente) clientes.get(i)).getDireccion();
            modelo.addRow(fila);
        }
        jTable1.setModel(modelo);
    }

